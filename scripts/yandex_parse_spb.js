#!/usr/bin/env node
/**
 * Парсер Москва, часть 2/4 (20 районов: Крюково — Северное Бутово)
 */
import { createParser, AREAS } from './yandex_parse_common.js';

const myAreas = new Set([
  "район Крюково", "район Кузьминки", "район Куркино",
  "район Левобережный", "район Лефортово", "район Лианозово",
  "район Люблино", "район Марфино", "район Марьина Роща",
  "район Марьино", "район Матушкино", "район Метрогородок",
  "район Митино", "район Москворечье-Сабурово", "район Нагатино-Садовники",
  "район Нагатинский Затон", "район Некрасовка", "район Новогиреево",
  "район Новокосино", "район Северное Бутово",
]);

const parser = createParser('yandex_parse_moscow_rev', {
  'Москва': AREAS['Москва'].filter((a) => myAreas.has(a.name)),
});

parser.run().catch((err) => { console.error('Fatal:', err); process.exit(1); });
