#!/usr/bin/env node
/**
 * Парсер Москва, часть 3/4 (20 районов: Северное Измайлово — Чертаново Южное)
 */
import { createParser, AREAS } from './yandex_parse_common.js';

const myAreas = new Set([
  "район Северное Измайлово", "район Северное Медведково", "район Северное Тушино",
  "район Северный", "район Силино", "район Сокол",
  "район Соколиная Гора", "район Сокольники", "район Солнцево",
  "район Старое Крюково", "район Строгино", "район Текстильщики",
  "район Хамовники", "район Ховрино", "район Хорошёво-Мнёвники",
  "район Царицыно", "район Черёмушки", "район Чертаново Северное",
  "район Чертаново Центральное", "район Чертаново Южное",
]);

const parser = createParser('yandex_parse_moscow3', {
  'Москва': AREAS['Москва'].filter((a) => myAreas.has(a.name)),
});

parser.run().catch((err) => { console.error('Fatal:', err); process.exit(1); });
