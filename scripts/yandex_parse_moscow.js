#!/usr/bin/env node
/**
 * Парсер Москва, часть 1/4 (20 районов: Вороново — Крылатское)
 */
import { createParser, AREAS } from './yandex_parse_common.js';

const myAreas = new Set([
  "район Вороново", "район Восточное Дегунино", "район Восточное Измайлово",
  "район Восточный", "район Выхино-Жулебино", "район Гольяново",
  "район Дорогомилово", "район Замоскворечье", "район Западное Дегунино",
  "район Зюзино", "район Зябликово", "район Ивановское",
  "район Измайлово", "район Капотня", "район Коммунарка",
  "район Коньково", "район Коптево", "район Косино-Ухтомский",
  "район Котловка", "район Крылатское",
]);

const parser = createParser('yandex_parse_moscow', {
  'Москва': AREAS['Москва'].filter((a) => myAreas.has(a.name)),
});

parser.run().catch((err) => { console.error('Fatal:', err); process.exit(1); });
