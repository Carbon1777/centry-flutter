#!/usr/bin/env node
/**
 * Парсер Москва, часть 4/4 (17 районов: Щербинка — Ярославский)
 */
import { createParser, AREAS } from './yandex_parse_common.js';

const myAreas = new Set([
  "район Щербинка", "район Щукино", "район Южное Бутово",
  "район Южное Медведково", "район Южное Тушино", "район Якиманка",
  "район Ясенево", "Рязанский район", "Савёловский район",
  "Таганский район", "Тверской район", "Тимирязевский район",
  "Тропарёво-Никулино", "Филимонковский район", "Хорошёвский район",
  "Южнопортовый район", "Ярославский район",
]);

const parser = createParser('yandex_parse_moscow4', {
  'Москва': AREAS['Москва'].filter((a) => myAreas.has(a.name)),
});

parser.run().catch((err) => { console.error('Fatal:', err); process.exit(1); });
